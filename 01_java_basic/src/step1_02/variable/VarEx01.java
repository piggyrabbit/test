package step1_02.variable;

public class VarEx01 {
	public static void main(String[] args) {
		
		int appleNumber;
		appleNumber = 3;
		System.out.println(appleNumber);
		
		double appleNumber2;
		appleNumber2 = 3.14;
		System.out.println(appleNumber2);
		
		char apple_number4;
		apple_number4 = 'a';
		System.out.println(apple_number4);
		
		String apple_number5;
		apple_number5 = "아무말";
		System.out.println(apple_number5);
	}
}
